function search() {
	const searchResult = document.getElementById("searchResult");
	searchResult.innerHTML = "";
	const searchBox = document.getElementById("search").value;

	for (const person of people) {
		if (person.name.toLowerCase().includes(searchBox)) {
			const resultName = document.createElement("h1");
			const resultID = document.createElement("h1");
			const resultEmail = document.createElement("h1");
			const space = document.createElement("hr");

			resultName.innerHTML = person.name;
			resultID.innerHTML = "ID#" + person.id;
			resultEmail.innerHTML = person.id + "@students.srvusd.net";

			searchResult.appendChild(resultName);
			searchResult.appendChild(resultID);
			searchResult.appendChild(resultEmail);
			searchResult.appendChild(space);
		}
	}
}

const input = document.getElementById("search");

input.addEventListener("keyup", function (event) {
	// Number 13 is the "Enter" key on the keyboard
	if (event.keyCode === 13) {
		event.preventDefault();
		search();
	}
});
